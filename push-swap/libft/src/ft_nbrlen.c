#include "../includes/libft.h"

size_t	ft_nbrlen(int n)
{
	size_t	i;

	i = 0;
	if (n < 0)
	{
		n = ABS(n);
		++i;
	}
	while (n /= 10)
		++i;
	return (i);
}
